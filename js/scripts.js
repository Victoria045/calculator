// Addition
var add = (number1, number2) => number1 + number2


var number1 = parseInt(prompt('Enter a number:'));
var number2 = parseInt(prompt('Enter the second number'));
alert(add(number1, number2));

// subtraction
var subtract = (number3, number4) => number3 - number4


var number3 = parseInt(prompt('Enter a number:'));
var number4 = parseInt(prompt('Enter the second number'));
alert(subtract(number3, number4));

